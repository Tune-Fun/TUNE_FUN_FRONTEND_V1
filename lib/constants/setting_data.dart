    1-2. 기타 회사가 추가 개발하거나 다른 회사와의 제휴계약 등을 통해 회원에게 제공하는 일체의 서비스

2. 회사가 회원의 이용신청을 승낙한 때부터 회원은 서비스를 이용할 수 있다. 단 일부 서비스의 경우에는 일정 요건 충족시에 서비스를 이용할 수 있다.
3. 회사의 업무상 또는 기술상의 장애로 인하여 서비스의 이용을 개시하지 못하는 경우에는 플랫폼 내 공시하거나 회원에게 이를 통지한다.
