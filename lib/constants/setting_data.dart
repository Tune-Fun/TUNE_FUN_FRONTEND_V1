1. 회사가 회원에게 제공하는 서비스에 대한 지식재산권을 포함한 일체의 권리는 회사에 귀속된다.

    1-1. 회원이 서비스를 이용하는 과정에서 작성한 게시물에 대한 저작권을 포함한 일체의 권리는 별도의 의사표시가 없는 한 각 회원에게 귀속된다.

2. 아티스트는 자신이 서비스에 게시한 게시물 등을 회사가 국내ㆍ외에서 다음 각 호의 목적으로 사용하는 것을 허락한다.

    2-1. 회사의 서비스를 홍보하기 위한 목적으로 미디어·통신사 등에 게시물의 전부 또는 일부를 보도·방영하게 하는 것. 이 경우 회사는 회원의 개별 동의 없이 미디어·통신사 등에게 회원정보를 제공하지 않음

3. 아티스트가 서비스에 게시물을 게재하는 것은 다른 회원이 게시물을 서비스 내에서 사용하거나 회사가 검색결과로 사용하는 것을 허락한 것으로 본다.
4. 회사는 서비스의 정책 또는 회사가 운영하는 서비스 간 통합 등의 사유로 게시물의 게재위치를 변경·이전할 수 있으며, 이 경우 사전에 공지한다.

# 제12조 게시물의 관리 및 임시조치

1. 회원의 게시물 등이 관련 법령에 위반되는 내용을 포함하는 경우, 회사는 해당 관련 법령이나 적법한 권리자의 요청에 따라 해당 게시물 등에 대한 게시중단 및 삭제 등의 조치를 취할 수 있다.
2. 회사는 게시물 등에 회사의 서비스 운영정책에 위반되는 사유가 있는 것으로 확인된 경우, 운영정책에 따라 해당 게시물에 대해 조치를 취할 수 있다.
3. 정보통신망 이용촉진 및 정보보호 등에 관한 법률(이하 정보통신망법)의 규정에 의해 다른 회원의 공개된 게시물 등이 본인의 사생활을 침해하거나 명예를 훼손하는 등 권리를 침해 받은 회원 또는 제3자(이하 "삭제 등 신청인")는 그 침해사실을 소명하여 회사에 해당 게시물 등의 삭제 또는 반박 내용의 게재를 요청할 수 있다. 이 경우 회사는 해당 게시물 등의 권리 침해 여부를 판단할 수 없거나 당사자 간의 다툼이 예상되는 경우 해당 게시물 등에 대한 접근을 임시적으로 차단하는 조치(이하 임시조치)를 최장 30일까지 취한다.
4. 전항에 의해 본인의 게시물 등이 임시조치된 아티스트(이하 게시자)는 임시조치기간 중 회사에 해당 게시물 등을 복원해 줄 것을 요청(이하 "재게시 청구")할 수 있으며, 회사는 임시조치된 게시물의 명예훼손 등 판단에 대한 방송통신심의위원회 심의 요청에 대한 게시자 및 삭제 등 신청인의 동의가 있는 경우 게시자 및 삭제 등 신청인을 대리하여 이를 요청하고 동의가 없는 경우 회사가 이를 판단하여 게시물 등의 복원 여부를 결정한다. 게시자의 재게시 청구가 있는 경우 임시조치 기간 내에 방송통신심의위원회 또는 회사의 결정이 있으면 그 결정에 따르고 그 결정이 임시조치 기간 내에 있지 않는 경우 해당 게시물 등은 임시조치 만료일 이후 복원된다. 재게시 청구가 없는 경우 해당 게시물 등은 임시조치 기간 만료 이후 삭제된다.
5. 회사는 서비스 내에 게시된 게시물 등이 사생활 침해 또는 명예훼손 등 제3자의 권리를 침해한다고 인정하는 경우 회원 또는 제3자의 신고가 없는 경우에도 임시조치를 취할 수 있다. 임시조치 된 게시물의 처리 절차는 본 조 제3항 후단 및 제4항의 규정에 따른다.
6. 회원의 게시물 등으로 인한 법률상 이익 침해를 근거로, 다른 회원 또는 제3자가 회원 또는 회사를 대상으로 하여 민·형사상의 법적조치(예: 형사고소, 가처분신청∙손해배상청구등민사소송의제기)를 취하는 경우, 회사는 동법적조치의 결과인 법원의 확정판결이 있을 때 까지 관련 게시물 등에 대한 접근을 잠정적으로 제한할 수 있다. 게시물 등의 접근제한과 관련한 법적조치의 소명, 법원의 확정판결에 대한 소명책임은 게시물 등에 대한 조치를 요청하는 자가 부담한다.

# 제13조 회사의 소유권

1. 회사가 제공하는 서비스, 그에 필요한 소프트웨어, 이미지, 마크, 로고, 디자인, 서비스 명칭, 정보 및 상표 등과 관련된 지적재산권 및 기타 권리는 회사에게 있다.
2. 회원은 회사가 명시적으로 승인한 경우를 제외하고는 제1항 소정의 각 재산에 대한 전부 또는 일부의 수정, 대여, 대출, 판매, 배포, 제작, 양도, 재 라이센스, 담보권설정행위, 상업적 이용행위를 할 수 없으며, 제3자로 하여금 이와 같은 행위를 하도록 허락할 수 없다.

# 제14조 회사의 의무

1. 회사는 법령과 본 약관이 금지하거나 미풍양속에 반하는 행위를 하지 않으며, 지속적 안정적으로 서비스를 제공하기 위하여 노력할 의무가 있다.
2. 회사는 회원의 개인 신상 정보를 본인의 승낙 없이 제3자에게 누설, 배포하지 않는다. 다만, 관계법령에 의해 국가기관의 요구나 전기통신관련법령 및 정보통신윤리위원회의 요청 등 법률의 규정에 따른 적법한 절차에 의한 경우에는 그러하지 아니한다.
3. 회사는 계속적이고 안정적인 서비스의 제공을 위하여 설비에 장애가 생기거나 멸실된 때에는 부득이한 사유가 없는 한 지체 없이 이를 수리 또는 복구한다.
4. 회사가 제공하는 서비스로 인하여 회원에게 손해가 발생한 경우 그러한 손해가 회사의 고의나 중과실에 기해 발생한 경우에 한하여 책임을 부담하며, 그 책임의 범위는 통상손해에 한한다.
5. 회사는 회원으로부터 제기되는 의견이나 불만이 정당하다고 인정할 경우에는 신속히 처리하여야 한다. 다만, 신속한 처리가 곤란한 경우에는 회원에게 그 사유와 처리일정을 통보하여야 한다.
6. 회사는 관련법령이 정하는 바에 따라서 회원 등록정보를 포함한 회원의 개인정보를 보호하기 위하여 노력한다. 회원의 개인정보보호에 관해서는 관련법령에 제시된 내용을 지킨다.

# 제15조 회원의 의무

1. 회원은 서비스 이용을 위해 가입할 경우 현재의 사실과 일치하는 완전한 정보 (이하 "가입정보")를 제공해야 하며, 가입정보가 변경된 경우 즉시 갱신해야 한다.
2. 회원은 본인의 아이디와 비밀번호 관리를 위해 제 8조 회원의 정보보안 사항을 준수하여야 한다.
3. 회원은 서비스를 이용 시, 각 호에 해당하는 행위를 해서는 안 된다.

    3-1. 타인(소수를 포함)에게 위해를 가하는 행위

    3-2. 타인의 아이디(ID), 비밀번호(PASSWORD) 도용 및 타인으로 가장하는 행위

    3-3. 타인과의 관계를 허위로 명시하는 행위

    3-4. 타인을 비방할 목적으로 사실 또는 허위의 사실을 적시하여 명예를 훼손하는 행위

    3-5. 자기 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 가할 목적으로 허위의 정보를 유통시키는 행위

    3-6. 수치심이나 혐오감 또는 공포심을 일으키는 말이나 음향, 글을 타인에게 전달하는 행위

    3-7. 회사의 사전 승낙 없이 서비스를 통하여 얻은 정보를 가공, 판매하는 행위 등 서비스를 이용한 영리행위

    3-8. 타인의 정보통신서비스 이용명의를 도용하여 사용하는 행위

    3-9. 불필요하거나 승인되지 않은 광고, 판촉물을 게재하거나, 미성년자가 볼 수 있는 곳에 저속 또는 음란한 데이터, 텍스트, 소프트웨어, 음악, 사진, 그래픽, 비디오 메시지 등을 게재하거나 이를 미성년자에게 전자우편으로 보내는 행위

    3-10. 권리(지적재산권을 포함한 모든 권리)가 없는 콘텐츠를 게재하거나 메시지 또는 전자우편으로 보내는 행위

    3-11. 컴퓨터 소프트웨어, 하드웨어, 전기통신 장비를 파괴, 방해 또는 기능을 제한하기 위한 소프트웨어 바이러스를 게재하거나 메시지 또는 전자우편으로 보내는 행위

    3-12. 다른 컴퓨터 코드, 파일, 프로그램을 포함하고 있는 자료를 게시, 게재, 전자우편으로 보내는 행위 등 다른 사용자의 개인정보를 수집 또는 저장하는 행위

    3-13. 재물을 걸고 도박하거나 사행행위를 하는 행위

    3-14. 윤락행위를 알선하거나 음행을 매개하는 내용의 정보를 유통시키는 행위

    3-15. 기타 불법적이거나 부당한 행위

4. 회원은 이 약관 및 관계법령에서 규정한 사항을 준수하여야 한다.

# 제16조 부정이용 금지

회사는 다음 각호에 해당하는 경우를 부정 이용행위로 본다.

1. 자신의 ID 등 회사가 제공하는 서비스를 타인에게 판매, 대여, 양도하는 행위 및 이를 광고하는 행위
2. 서비스 이용 중, 복제프로그램을 실행하는 경우
3. 콘텐츠의 DRM(Digital Rights Management)을 무력화하는 일체의 행위

# 제17조 회원에 대한 통지

1. 회사가 회원에 대한 통지를 하는 경우, 회원이 회사와 미리 약정하여 지정한 전자우편 주소, 메시지, 전화로 할 수 있다.
2. 회사는 불특정 다수 회원에 대한 통지의 경우, 1주일 이상 플랫폼 내 게시함으로써 개별 통지에 갈음할 수 있다. 다만, 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별 통지한다.

# 제18조 개별 서비스에 대한 약관 및 이용조건

회사는 제공하는 서비스의 구체적 내용에 따라 개별 서비스에 대한 약관 및 이용조건을 각 개별서비스마다 별도로 정하여 회원의 동의를 얻을 수 있다. 이 경우 개별 서비스에 대한 이용약관 등이 본 약관에 우선한다.

# 제19조 제 3자의 지적재산권 및 저작권 공지

1. 회사와 회원은 제 3자의 지적재산을 존중하며, 회사는 언제든지 제 3자의 지적 재산을 침해하는 회원의 ID를 경고 및 정지 할 수 있다.
2. 회사는 아티스트가 게재한 게시물이 제 3자의 저작권을 침해했다고 통지 받는 경우, 저작권법 및 관련법령에 따라 해당 자료를 서비스에서 제거할 수 있다.
3. 자신의 저작권이 침해되었다고 믿는 제 3자의 경우, 다음 정보를 포함하는 서면 통지를 작성하여 회사에 제공하여야 한다.

    3-1. 저작권 소유자 또는 법적 대리인의 전자 서명 혹은 실질 서명

    3-2. 침해를 주장하는 게시물 및 콘텐츠

    3-3. 침해를 주장하는 게시물 및 콘텐츠의 게시 혹은 사용된 위치에 대한 상세한 기술

    3-4. 저작권 소유자 또는 그 법적 대리인의 주소, 전화번호 및 이메일 주소

    3-5. 침해를 주장하는 서면 통지상의 모든 정보가 사실과 부합하며, 위증으로 판명될 경우 법적 책임을 지겠다는 저작권 소유자 또는 법적 대리인의 진술

4. 회원은 자신의 콘텐츠가 실수 또는 오인으로 인해 삭제 혹은 접근불가상태가 되었다고 믿는 경우, 다음 정보를 포함하는 서면 통지를 작성하여 회사에 제공하여야 한다.

    4-1. 회원의 전자서명 혹은 실질 서명

    4-2. 삭제 혹은 접근불가상태가 된 콘텐츠가 게시되어 있던 위치에 대한 상세한 기술

    4-3. 실수 또는 오인의 결과로 자신의 콘텐츠가 삭제 혹은 접근불가상태가 되었다고 믿는 진술과 이와 같은 진술이 위증으로 판명될 경우 법적 책임을 지겠다는 서약

    4-4. 회원의 성명, 주소, 전화번호 및 이메일 주소

5. 회사는 본 조항과 관련된 제 3자 또는 회원의 통지가 위에서 기술한 요구사항에 부합하지 않을 경우, 해당 통지는 유효하지 않은 것으로 간주할 수 있으며, 이에 대해 응하거나 회답해야 할 책임이 없다.

# 제20조 회원탈퇴 및 자격상실

1. 회원은 회사에 언제든지 회원탈퇴를 요청할 수 있으며, 회사는 회원탈퇴에 관한 규정에 따라 이를 처리한다.
2. 회사는 회원이 다음 각 호의 사유에 해당하는 경우, 사전통지 없이 회원자격을 제한 및 정지시킬 수 있다.

    2-1. 가입신청 시에 허위 내용을 등록한 경우

    2-2. 다른 사람의 서비스 이용을 방해하거나 그 정보를 도용하는 등 전자거래질서를 위협하는 경우

    2-3. 회사를 이용하여 법령과 이 약관이 금지하거나 공공질서 미풍양속에 반하는 행위를 하는 경우

3. 회사가 회원자격을 제한, 정지시킨 후 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 회사는 회원자격을 상실시킬 수 있다.
4. 회사가 회원자격을 상실시키는 경우에는 회원등록을 말소한다. 이 경우 회원에게 이를 통지하고, 회원등록 말소 전에 소명할 기회를 부여한다.
5. 회사는 회원이 이 약관의 내용에 위배되는 행동을 한 경우, 임의로 서비스 사용을 제한 및 중지하거나 본인의 동의 없이 이용계약을 해지할 수 있다. 이 경우 회사는 회원의 접속을 금지할 수 있으며, 회원이 게시한 내용의 전부 또는 일부를 임의로 삭제 할 수 있다.

# 제21조 양도금지

회원은 서비스의 이용권한, 기타 이용계약상의 지위를 타인에게 양도 및 증여할 수 없으며, 이를 담보로 제공할 수 없다. 그러나 회원과 제 3자간 상호 동의 하에 서비스 이용 및 관리를 위해 이용권한이 필요한 경우에는 이를 양도할 수 있으나 이에 대해 회사에 사전 서면 공유해야 한다.

# 제22조 손해배상

